Rune
tobaserune(Rune c)
{
	Rune *p;

	p = _runebsearch(c, _base2, nelem(_base2)/2, 2);
	if(p && c == p[0])
		c = p[1];
	return c;
}

int
isbaserune(Rune c)
{
	return tobaserune(c) == c;
}
